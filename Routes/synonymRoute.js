// 📋 LIST SYNONYMS
// =========================
router.get("/list", async (req, res) => {

  try {

    const { store } = req.query;

    const normalizedStore =
      store?.trim()?.toLowerCase();

    if (!store) {

      return res.status(400).json({
        error: "Store is required"
      });

    }

    const synonyms =
      await Synonym
        .find({
          store:
            normalizedStore
        })
        .sort({
          createdAt: -1
        })
        .lean();

    res.json({
      synonyms
    });

  } catch (err) {

    console.error(err);

    res.status(500).json({
      error: err.message
    });

  }

});

